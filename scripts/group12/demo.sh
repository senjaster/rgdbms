#!/bin/sh

SCRIPT_DIR="$(cd "$(dirname "$0")" && pwd)"
. "${SCRIPT_DIR}/../lib.sh"

# ============================================
# КРИТЕРИЙ 58: Вертикально: параллельные планы выполнения
# ============================================

header "КРИТЕРИЙ 58: Вертикально: параллельные планы выполнения"

comment "YDB практически всегда выполняет "тяжелые" запросы распределенно, загружая несколько узлов кластера".
comment "Однако и внутри каждого узла задействуется более одного ядра."
comment ""
comment "Проверим вначале нагрузку на кластер:"
link "https://10.40.13.22:8765/monitoring/tenant?tenantPage=diagnostics&diagnosticsTab=nodes&database=%2FRoot%2Fdatabase&schema=%2FRoot%2Fdatabase"
pause

comment "Выполним запрос с CROSS JOIN и будем наблюдать за потреблением CPU"

run "ydb -p default sql -s 'SELECT count(*) FROM item i1 CROSS JOIN item i2'"

pause

# ============================================
# КРИТЕРИЙ 59: Вертикально: учет NUMA-нод при аллокации памяти
# ============================================

header "КРИТЕРИЙ 59: Вертикально: учет NUMA-нод при аллокации памяти"

comment "В целом, мы не рекомендуем развертывать YDB на \"больших\" серверах, состоящих из нескольких NUMА-нод."
comment "YDB хорошо масштабируется горизонтально и как правило дешевле использовать два односокетных сервера"
comment "чем один двухсокетный."
comment ""
comment "Тем не менее, запуск на серверах с NUMA возможен, поскольку на крупных узлах мы в любом случае"
comment "рекомендуем запускать несколько процессов баз данных вместо одного большого."
comment "В случае NUMA важно позаботиться о том, чтобы каждый из процессов получил ядра и память,"
comment "относящиеся к одной NUMA-ноде."
comment ""
comment "Systemd позволяет задать это в юнит-файле:"
comment ""
comment "Для привязки процесса к определенным CPU ядрам и NUMA-ноде используются следующие опции:"
comment ""
comment "1. CPUAffinity - указывает список CPU ядер для процесса"
comment "2. NUMAPolicy - определяет политику выделения памяти (bind, preferred, interleave)"
comment "3. NUMAMask - указывает NUMA-ноды для выделения памяти"
comment ""
comment "Например:"
comment ""
comment "[Service]"
comment "CPUAffinity=0-15"
comment "NUMAPolicy=bind"
comment "NUMAMask=0"
comment ""
comment "Это привяжет процесс к ядрам 0-15 и NUMA-ноде 0"
comment "Каждому из процессов нужно выделить свои ядра и свою NUMA-ноду."

pause
